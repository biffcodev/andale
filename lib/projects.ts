import type { Lang } from "./i18n";
import imageManifest from "./projects.images.json";
import translations from "./projects.i18n.json";

/* EN/PT/IT translations of each project, keyed by slug. Generated from the
   Spanish source and kept in a separate data file so the copy is easy to
   review and replace. Missing slugs/languages fall back to Spanish. */
const TRANSLATIONS = translations as Record<string, Partial<Record<Lang, ProjectContent>>>;

/* ---------------------------------------------------------------------------
   Project content model.

   One entry per real project. Text lives per-language under `content`; when a
   language is missing the accessor falls back to Spanish (the source language),
   so the site works in all four languages while translations are filled in.

   Images are referenced by convention at /uploads/projects/<slug>/… and degrade
   to the striped placeholder until the real files are dropped in public/.
--------------------------------------------------------------------------- */

export interface ProjectContent {
  /** Short descriptor shown as the card/panel headline — no client name, no
      "Branding/Rebranding" (the client is always shown next to it). e.g.
      "Comunicación sin barreras" */
  title: string;
  /** "Frase descriptiva del proyecto" */
  tagline: string;
  /** Hero phrase */
  heroPhrase: string;
  /** Overview body */
  summary: string;
  /** The challenge */
  challenge: string;
  /** Our approach — one entry per paragraph */
  approach: string[];
  /** The outcome — one entry per paragraph */
  outcome: string[];
  /** "Frases sobre el proyecto" */
  quotes: string[];
  /** Discipline tags, e.g. "Branding · Estrategia" */
  tags: string;
  /** Sector, e.g. "Tecnología" */
  sector: string;
  /** City, when known */
  location?: string;
}

export interface Project {
  slug: string;
  client: string;
  year?: string;
  cover: string;
  /** Animated cover (a .gif) used ONLY on the individual project page's hero.
      The home rail and the archive keep the static `cover` image. */
  coverAnimated?: string;
  gallery: string[];
  video?: string;
  content: Partial<Record<Lang, ProjectContent>>;
}

/* Images per project come from lib/projects.images.json, generated by
   scripts/build-image-manifest.mjs scanning public/uploads/projects/<slug>/.
   Drop files there with any names (see docs/ASSETS.md) — the manifest is
   rebuilt automatically before every build/dev run. Until a project has real
   files it falls back to the by-convention placeholder paths, which degrade to
   the striped placeholder. `cover` is the first image; `gallery` is the rest
   (the detail page uses the first four and shows any extras in a closing grid). */
const MANIFEST = imageManifest as Record<string, { images: string[]; hero?: string }>;

const img = (slug: string) => {
  const entry = MANIFEST[slug];
  const files = entry?.images ?? [];
  const coverAnimated = entry?.hero;
  if (files.length === 0) {
    return {
      cover: `/uploads/projects/${slug}/cover.webp`,
      gallery: [1, 2, 3, 4].map((n) => `/uploads/projects/${slug}/${n}.webp`),
      coverAnimated,
    };
  }
  const cover = files[0];
  const rest = files.length > 1 ? files.slice(1) : files;
  return { cover, gallery: rest, coverAnimated };
};

