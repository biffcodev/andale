export interface LocalizedProject extends ProjectContent {
  slug: string;
  client: string;
  year?: string;
  cover: string;
  coverAnimated?: string;
  gallery: string[];
  video?: string;
  index: number;
  num: string;
  total: string;
}

function pickContent(p: Project, lang: Lang): ProjectContent {
  const translated = TRANSLATIONS[p.slug]?.[lang];
  return (translated || p.content[lang] || p.content.ES || p.content.EN || Object.values(p.content)[0]) as ProjectContent;
}

export function getProjects(lang: Lang): LocalizedProject[] {
  const total = (PROJECTS.length < 10 ? "0" : "") + PROJECTS.length;
  return PROJECTS.map((p, i) => ({
    ...pickContent(p, lang),
    slug: p.slug,
    client: p.client,
    year: p.year,
    cover: p.cover,
    coverAnimated: p.coverAnimated,
    gallery: p.gallery,
    video: p.video,
    index: i,
    num: (i < 9 ? "0" : "") + (i + 1),
    total,
  }));
}

export function getProject(slug: string, lang: Lang): LocalizedProject | undefined {
  return getProjects(lang).find((p) => p.slug === slug);
}
